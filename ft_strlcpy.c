#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dest_size)
{
	size_t	i;

	if (dest_size == 0)
		return (ft_strlen(src));
	i = 0;
	while (i < dest_size - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}
